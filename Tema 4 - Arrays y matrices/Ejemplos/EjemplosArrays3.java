import java.util.Arrays;

public class EjemplosArrays3 {

    public static void main(String[] args) {

        int[] array = { 1, 4, 5, 6, 7, 8, 3, 8 };
        for (int n : array) {
            System.out.print(n+" ");
        } 
        System.out.println();
        

        System.out.println(Arrays.toString(array)); 
        

    }
    
}
